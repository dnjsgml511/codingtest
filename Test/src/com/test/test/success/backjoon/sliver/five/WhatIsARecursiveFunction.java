package com.test.test.success.backjoon.sliver.five;

// https://www.acmicpc.net/problem/17478
import java.io.BufferedReader;
import java.io.BufferedWriter;
import java.io.InputStreamReader;
import java.io.OutputStreamWriter;

public class WhatIsARecursiveFunction {

	static BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(System.out));
	static int count;

	public static void main(String[] args) throws Exception {
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));

		count = Integer.parseInt(br.readLine());

		bw.write("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.");
		bw.write("\n");
		
		recursiveFunction(0);

		bw.flush();
		bw.close();
	}

	static void recursiveFunction(int loop) throws Exception {

		StringBuilder underbar = new StringBuilder();
		for(int i = 0; i < loop; i++) {
			underbar.append("____");
		}
		
		if (loop == count) {
			bw.write(underbar.toString());
			bw.write("\"재귀함수가 뭔가요?\"");
			bw.write("\n");
			
			bw.write(underbar.toString());
			bw.write("\"재귀함수는 자기 자신을 호출하는 함수라네\"");
			bw.write("\n");
			
			bw.write(underbar.toString());
			bw.write("라고 답변하였지.");
			bw.write("\n");
			return;
		}

		bw.write(underbar.toString());
		bw.write("\"재귀함수가 뭔가요?\"");
		bw.write("\n");

		bw.write(underbar.toString());
		bw.write("\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.");
		bw.write("\n");

		bw.write(underbar.toString());
		bw.write("마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.");
		bw.write("\n");

		bw.write(underbar.toString());
		bw.write("그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"");
		bw.write("\n");

		recursiveFunction(loop + 1);

		bw.write(underbar.toString());
		bw.write("라고 답변하였지.");
		bw.write("\n");
	}

}
